function inputAnalyzer(): string {
  return `import config from '../../agent.config.js';
import type { AgentInput, AnalyzedInput } from '../types/agent.js';

export function analyzeInput(input: AgentInput): AnalyzedInput {
  const normalizedInput = input.input.trim();
  const detectedEntities: Record<string, unknown> = {};
  const missingFields: string[] = [];
  const riskFlags: string[] = [];

  if (!normalizedInput) missingFields.push('input');
  if (/天气|weather/i.test(normalizedInput)) detectedEntities.possibleTool = 'weather.query';
  if (/计算|calculate|[+\\-*/()]/i.test(normalizedInput)) detectedEntities.possibleTool = 'math.calculate';

  for (const keyword of config.constraints.blockedKeywords) {
    if (normalizedInput.includes(keyword)) riskFlags.push(keyword);
  }

  return { rawInput: input.input, normalizedInput, detectedEntities, missingFields, riskFlags };
}
`;
}

function intentRouter(): string {
  return `import type { AnalyzedInput, AgentIntent } from '../types/agent.js';

export function routeIntent(input: AnalyzedInput): AgentIntent {
  if (input.missingFields.includes('input')) {
    return { name: 'ask_clarification', confidence: 1, reason: 'Input is empty.' };
  }
  if (input.riskFlags.length > 0) {
    return { name: 'safe_redirect', confidence: 1, reason: 'Input matched blocked keywords.' };
  }
  if (typeof input.detectedEntities.possibleTool === 'string') {
    return { name: 'call_tool', confidence: 0.86, reason: 'Input indicates a tool call.' };
  }
  return { name: 'generate_response', confidence: 0.7, reason: 'Default response intent.' };
}
`;
}

function guard(): string {
  return `import type { AgentIntent, AnalyzedInput } from '../types/agent.js';

export interface GuardResult {
  allowed: boolean;
  reason?: string;
  action: 'continue' | 'safe_redirect';
  warnings?: string[];
}

const allowedIntents = ['generate_response', 'call_tool', 'ask_clarification', 'safe_redirect'];

export function runGuard(analyzedInput: AnalyzedInput, intent: AgentIntent): GuardResult {
  if (!allowedIntents.includes(intent.name)) {
    return { allowed: false, reason: 'Intent is not allowed.', action: 'safe_redirect' };
  }
  if (analyzedInput.riskFlags.length > 0) {
    return {
      allowed: false,
      reason: \`Blocked keyword matched: \${analyzedInput.riskFlags.join(', ')}\`,
      action: 'safe_redirect',
      warnings: analyzedInput.riskFlags,
    };
  }
  return { allowed: true, action: 'continue' };
}
`;
}

function planner(): string {
  return `import type { AgentContext, AgentPlan } from '../types/agent.js';
import { createId } from '../utils/id.js';

export function createPlan(context: AgentContext): AgentPlan {
  if (context.intent.name === 'safe_redirect') {
    return { goal: 'Return safe redirect', steps: [{ id: createId('step'), type: 'guard', name: 'safe_redirect', input: {} }] };
  }
  if (context.intent.name === 'ask_clarification') {
    return { goal: 'Ask clarification', steps: [{ id: createId('step'), type: 'llm', name: 'ask_clarification', input: context.input.input }] };
  }
  if (context.intent.name === 'call_tool') {
    const toolName = String(context.analyzedInput.detectedEntities.possibleTool);
    const input = toolName === 'math.calculate'
      ? { expression: extractExpression(context.analyzedInput.normalizedInput) }
      : { location: extractLocation(context.analyzedInput.normalizedInput), date: 'tomorrow' };
    return { goal: \`Call \${toolName} tool\`, steps: [{ id: createId('step'), type: 'tool', name: toolName, input }] };
  }
  return { goal: 'Generate response', steps: [{ id: createId('step'), type: 'llm', name: 'generate_response', input: context.input.input }] };
}

function extractExpression(input: string): string {
  const match = input.match(/[0-9\\s+\\-*/().]+/);
  return match?.[0]?.trim() || '1 + 1';
}

function extractLocation(input: string): string {
  const words = input.split(/\\s+/).filter(Boolean);
  return words.find((word) => !/天气|weather|明天|tomorrow/i.test(word)) || 'Tokyo';
}
`;
}

function executor(): string {
  return `import type { AgentOutput, AgentPlan } from '../types/agent.js';
import type { TraceLogger } from '../traces/traceLogger.js';
import { getToolRegistry } from './toolRegistry.js';
import { recoverError } from './errorRecovery.js';
import { mockLLM } from '../model/mockLLM.js';

export async function executePlan(plan: AgentPlan, traceId: string, sessionId: string | undefined, trace: TraceLogger): Promise<AgentOutput> {
  for (const step of plan.steps) {
    try {
      if (step.type === 'guard') {
        return { success: false, intent: 'safe_redirect', message: 'Sorry, I cannot complete that request safely.', traceId };
      }
      if (step.type === 'tool') {
        trace.append({ toolCalls: [{ name: step.name, input: step.input }] });
        const data = await getToolRegistry().executeTool(step.name, step.input, { traceId, sessionId });
        trace.append({ toolResults: [{ name: step.name, data }] });
        return { success: true, intent: 'call_tool', message: \`Tool \${step.name} executed successfully.\`, data, traceId };
      }
      if (step.type === 'llm') {
        const result = await mockLLM.generate({ task: step.name, input: step.input });
        return { success: true, intent: step.name, message: result.text, data: result.data, traceId };
      }
    } catch (error) {
      const recovered = recoverError(error);
      if (recovered.output) return { ...recovered.output, traceId };
      return { success: false, intent: step.name, message: recovered.reason, errors: [recovered.reason], traceId };
    }
  }
  return { success: false, intent: 'unknown', message: 'No executable plan steps found.', traceId };
}
`;
}

function orchestrator(): string {
  return `import config from '../../agent.config.js';
import type { AgentInput, AgentOutput, AgentContext } from '../types/agent.js';
import { analyzeInput } from './inputAnalyzer.js';
import { routeIntent } from './intentRouter.js';
import { runGuard } from './guard.js';
import { createPlan } from './planner.js';
import { executePlan } from './executor.js';
import { validateOutput } from './outputValidator.js';
import { createTraceLogger } from '../traces/traceLogger.js';
import { createId } from '../utils/id.js';
import { getToolRegistry } from './toolRegistry.js';

export async function runAgent(input: AgentInput): Promise<AgentOutput> {
  const traceId = createId('trace');
  const requestId = createId('req');
  const trace = createTraceLogger(config.trace, traceId, requestId, input.input);

  try {
    const analyzedInput = analyzeInput(input);
    const intent = routeIntent(analyzedInput);
    trace.append({ detectedIntent: intent });

    const guard = runGuard(analyzedInput, intent);
    const effectiveIntent = guard.allowed ? intent : { name: 'safe_redirect' as const, confidence: 1, reason: guard.reason };

    const context: AgentContext = {
      input,
      analyzedInput,
      intent: effectiveIntent,
      availableTools: getToolRegistry().listTools().map((tool) => tool.name),
    };

    const plan = createPlan(context);
    trace.append({ plan });
    const output = await executePlan(plan, traceId, input.sessionId, trace);
    const validated = validateOutput(output, traceId);
    trace.end(validated);
    return validated;
  } catch (error) {
    const fallback = validateOutput({
      success: false,
      intent: 'unknown_error',
      message: 'Agent recovered from an unexpected error.',
      errors: [error instanceof Error ? error.message : String(error)],
      traceId,
    }, traceId);
    trace.append({ errors: [fallback.errors] });
    trace.end(fallback);
    return fallback;
  }
}
`;
}

function toolRegistry(): string {
  return `import type { ToolContext, ToolDefinition } from '../types/tool.js';
import { weatherTool } from '../tools/weather.js';
import { mathTool } from '../tools/math.js';

export class ToolRegistry {
  private tools = new Map<string, ToolDefinition>();

  registerTool(tool: ToolDefinition): void {
    this.tools.set(tool.name, tool);
  }

  getTool(name: string): ToolDefinition {
    const tool = this.tools.get(name);
    if (!tool) throw new Error(\`tool_not_found: \${name}\`);
    return tool;
  }

  listTools(): ToolDefinition[] {
    return [...this.tools.values()];
  }

  hasTool(name: string): boolean {
    return this.tools.has(name);
  }

  async executeTool(name: string, input: unknown, context: ToolContext): Promise<unknown> {
    const tool = this.getTool(name);
    const parsedInput = tool.inputSchema.safeParse(input);
    if (!parsedInput.success) throw new Error(\`tool_input_invalid: \${parsedInput.error.message}\`);
    const output = await tool.handler(parsedInput.data, context);
    const parsedOutput = tool.outputSchema.safeParse(output);
    if (!parsedOutput.success) throw new Error(\`tool_output_invalid: \${parsedOutput.error.message}\`);
    return parsedOutput.data;
  }
}

const registry = new ToolRegistry();
registry.registerTool(weatherTool);
registry.registerTool(mathTool);

export function getToolRegistry(): ToolRegistry {
  return registry;
}
`;
}

